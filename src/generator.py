import time
from cerebras.cloud.sdk import Cerebras

client = Cerebras(api_key=os.environ["CEREBRAS_API_KEY"])


# ── Helpers ───────────────────────────────────────────────────────────────────

def _call(messages: list, max_tokens: int = 2048, temperature: float = 0.5) -> str:
    """Call Cerebras with automatic retry on 429 rate limit."""
    max_retries = 5
    for attempt in range(max_retries):
        try:
            response = client.chat.completions.create(
                model="gpt-oss-120b",
                messages=messages,
                max_tokens=max_tokens,
                temperature=temperature,
            )
            return response.choices[0].message.content.strip()
        except Exception as e:
            err = str(e)
            if "429" in err or "too_many_requests" in err or "queue_exceeded" in err:
                wait = (attempt + 1) * 8
                print(f"  [rate limit] waiting {wait}s before retry {attempt + 1}/{max_retries}...")
                time.sleep(wait)
                continue
            raise
    raise RuntimeError("Cerebras rate limit: max retries exceeded")


def _repair_json(raw: str) -> str:
    """
    Trim back to the last complete top-level closing brace/bracket so that
    a response truncated mid-stream by a token limit can still be parsed.
    Tries } first (objects), then ] (arrays).
    """
    for closing in ("}", "]"):
        idx = raw.rfind(closing)
        if idx != -1:
            candidate = raw[: idx + 1]
            try:
                json.loads(candidate)
                return candidate
            except json.JSONDecodeError:
                continue
    return raw


def _parse_json(raw: str) -> dict | list:
    """Strip markdown fences then parse JSON, with truncation recovery."""
    if raw.startswith("```"):
        parts = raw.split("```")
        raw = parts[1] if len(parts) > 1 else raw
        if raw.startswith("json"):
            raw = raw[4:]
    raw = raw.strip()

    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return json.loads(_repair_json(raw))


def _source_context_block(analysis: dict) -> str:
    ctx = analysis.get("_source_context", {})
    if not ctx:
        return ""
    block = "REFERENCE — ACTUAL SOURCE CODE FROM TRENDING REPO (read patterns, don't copy):\n"
    block += f"\nDirectory structure:\n{ctx.get('tree_summary', '')[:800]}\n"
    for path, content in ctx.get("source_files", {}).items():
        block += f"\n--- {path} ---\n{content[:1200]}\n"
    return block


