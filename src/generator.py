"""
Three-phase project generator using Cerebras.

Phase 1: Plan     — architecture + file tree, category-aware, informed by source code read
Phase 2: Generate — each file written individually with full context
Phase 3: Validate — static review pass: broken imports, TODOs, name mismatches

No AI watermarks. No "generated by". Comments read like a human wrote them.
"""

import os
import json
import time
from cerebras.cloud.sdk import Cerebras

client = Cerebras(api_key=os.environ["CEREBRAS_API_KEY"])


# ── Helpers ───────────────────────────────────────────────────────────────────

def _call(messages: list, max_tokens: int = 2048, temperature: float = 0.5) -> str:
    response = client.chat.completions.create(
        model="llama-4-scout-17b-16e-instruct",
        messages=messages,
        max_tokens=max_tokens,
        temperature=temperature,
    )
    return response.choices[0].message.content.strip()


def _parse_json(raw: str) -> dict | list:
    if raw.startswith("```"):
        parts = raw.split("```")
        raw = parts[1] if len(parts) > 1 else raw
        if raw.startswith("json"):
            raw = raw[4:]
    return json.loads(raw.strip())


def _source_context_block(analysis: dict) -> str:
    """
    Build a compact block of the trending repo's actual source code
    to give the generator real implementation patterns to learn from.
    """
    ctx = analysis.get("_source_context", {})
    if not ctx:
        return ""

    block = "REFERENCE — ACTUAL SOURCE CODE FROM TRENDING REPO (read patterns, don't copy):\n"
    block += f"\nDirectory structure:\n{ctx.get('tree_summary', '')[:800]}\n"

    for path, content in ctx.get("source_files", {}).items():
        block += f"\n--- {path} ---\n{content[:1200]}\n"

    return block


