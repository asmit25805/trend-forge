"""
Three-phase project generator using Cerebras.

Phase 1: Plan     — architecture + file tree, category-aware, informed by source code
Phase 2: Generate — each file written individually with retry on 429
Phase 3: Validate — static review: broken imports, TODOs, name mismatches

No AI watermarks. No "generated by". Comments read like a human wrote them.
"""

import os
